                <h3 class="box-title"><i class="fa fa-gg-circle"></i> <strong>Bentuk Covid-19 (Virus Corona) ?</strong></h3>
                <div class="row">
                    <div class="col-md-6">
                        <p>Coronavirus memiliki genom RNA untai tunggal yang luar biasa besar, panjangnya sekitar 26.000 hingga 32.000 basa atau “huruf” RNA. Partikel bentuk virus corona dikelilingi oleh lapisan luar berlemak yang disebut amplop dan biasanya tampak bulat, seperti yang terlihat di bawah mikroskop elektron, dengan mahkota atau "korona" paku berbentuk tongkat di permukaannya.</p>
                        <p>SARS-CoV-2 adalah salah satu dari banyak virus corona. Semua virus ini berbagi karakteristik, memiliki genom RNA untai tunggal terbesar di alam. Genom ini berisi semua kode genetik yang dibutuhkan virus untuk memproduksi protein, menghindari sistem kekebalan dan bereplikasi di dalam tubuh manusia. Sebagian besar informasi itu terkandung dalam struktur 3D yang diadopsi oleh genom RNA ini ketika menginfeksi sel, dikutip dari sciencedaily.com.</p>
                    </div>
                    <div class="col-md-6">
                        <p>Dalam sebuah penelitian yang diterbitkan di jurnal Molecular Cell, tim mengungkap seluruh struktur genom bentuk virus corona SARS-CoV-2 di dalam sel inang, mengungkapkan jaringan interaksi RNA-RNA yang mencakup bagian genom yang sangat panjang.</p>
                        <p>Bagian fungsional yang berbeda di sepanjang genom perlu bekerja sama meskipun ada jarak yang sangat jauh di antara mereka, dan data struktural baru menunjukkan bagaimana hal ini dilakukan untuk mengaktifkan siklus hidup bentuk virus corona ini dan menyebabkan penyakit.</p>
