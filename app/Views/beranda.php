                <h3 class="box-title"><i class="fa fa-viadeo-square"></i> <strong>Apa itu Covid-19 (Virus Corona) ?</strong></h3>
                <div class="row">
                    <div class="col-md-6">
                        <p>Virus Corona atau severe acute respiratory syndrome coronavirus 2 (SARS-CoV-2) adalah virus yang menyerang sistem pernapasan. Penyakit akibat infeksi virus ini disebut COVID-19. Virus Corona bisa menyebabkan gangguan ringan pada sistem pernapasan, infeksi paru-paru yang berat, hingga kematian.</p>
                        <p>Severe acute respiratory syndrome coronavirus 2 (SARS-CoV-2) atau yang lebih dikenal dengan nama virus Corona adalah jenis baru dari Coronavirus yang menular ke manusia. Virus ini dapat menyerang siapa saja, mulai dari lansia (golongan usia lanjut), orang dewasa, anak-anak dan bayi, sampai ibu hamil dan ibu menyusui.</p>
                    </div>
                    <div class="col-md-6">
                        <p>Infeksi virus Corona yang disebut COVID-19 (Corona Virus Disease 2019) pertama kali ditemukan di kota Wuhan, China pada akhir Desember 2019. Virus ini menular dengan sangat cepat dan menyebar ke hampir semua negara, termasuk Indonesia, hanya dalam waktu beberapa bulan.</p>
                        <p>Hal tersebut membuat beberapa negara memberlakukan kebijakan lockdown untuk mencegah virus Corona makin meluas. Di Indonesia, pemerintah menerapkan kebijakan Pemberlakuan Pembatasan Kegiatan Masyarakat (PPKM) untuk menekan penyebaran virus ini.</p>
