                        <h3 class="box-title"><i class="fa fa-vimeo"></i> <strong>Penyebaran Covid-19</strong></h3>
                    </div>
                </div>
                <br>
                <div class="row">
                    <div class="col-md-6">
                        <p>Peningkatan jumlah pasien yang terinfeksi disebabkan oleh bagaimana cara virus tersebut menyebar. Melansir dari WHO, virus COVID-19 dapat menyebar melalui beberapa cara berikut:</p>
                        <p>
                        <ul>
                            <li><strong>Melalui Droplet</strong> - Droplet adalah cairan atau percikan air yang keluar dari saluran pernapasan ketika seseorang batuk maupun bersin. Risiko penularan virus COVID-19 melalui droplet akan meningkat drastis apabila seseorang tidak mengenakan masker. Namun ternyata, droplet tidak hanya sebatas cairan yang dikeluarkan ketika bersin atau batuk, melainkan juga ketika berbicara, bernyanyi, maupun tertawa.</li>
                            <li><strong>Melalui Kontak Fisik</strong> - Kontak fisik seperti berjabat tangan adalah salah satu media penularan COVID-19, karena kita tidak pernah tahu ada berapa banyak kuman, virus, maupun bakteri ditangan kita dan lawan bicara. Makanya, sebisa mungkin hindari kontak fisik secara langsung. Kalau kamu bingung, coba untuk mengganti model jabat tangan dengan gerakan Namaste, yaitu gerakan mengatupkan kedua tangan di dada yang kerap digunakan saat melakukan olahraga yoga.</li>
                            <li><strong>Melalui Permukaan yang Terkontaminasi</strong> - Penularan virus COVID-19 bisa terjadi saat seseorang menyentuh barang yang mungkin saja sudah terkontaminasi oleh droplet orang lain. Lalu, virus tersebut berpindah ke hidung, mulut, atau mata dari sentuhan barang yang terkontaminasi tadi. Inilah alasan pentingnya kita harus rajin mencuci tangan menggunakan sabun dan air mengalir setelah melakukan aktivitas apapun, termasuk menyentuh barang meski hanya sebentar saja.</li>
                        </ul>
                        </p>
                    </div>
                    <div class="col-md-6">
                        <p>
                        <ul>
                            <li><strong>Ruangan dengan Ventilasi Buruk</strong> - Ruangan tertutup dengan ventilasi yang kurang baik menjadi tempat nyaman untuk penyebaran virus. Virus COVID-19 dapat menyebar secara cepat apabila seseorang terlalu lama berada di dalam ruangan dengan ventilasi yang buruk. Maka dari itu, bukalah jendela ruangan dan biarkan udara segar memenuhi ruangan untuk mengurangi risiko penularan.</li>
                            <li><strong>Tempat Ramai</strong> - Menghindari tempat ramai menjadi satu dari sekian banyak upaya yang bisa dilakukan untuk mengurangi penularan. Tempat yang dipenuhi oleh orang-orang berisiko tinggi karena dapat memungkinkan terjadinya sentuhan fisik atau droplet yang beterbangan. Menjaga jarak minimal 1 meter adalah langkah pencegahan yang bisa kamu lakukan ketika sedang berada dalam situasi yang ramai.</li>
                        </ul>
                        </p>
